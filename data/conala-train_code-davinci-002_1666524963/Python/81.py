# Create a list of integers
my_list = [1, 2, 3, 4, 5]

# Print the list
print(my_list)

# Print the first element of the list
print(my_list[0])

# Print the last element of the list
print(my_list[-1])

# Print the second to last element of the list
print(my_list[-2])

# Print the first three elements of the list
print(my_list[0:3])

# Print the last three elements of the list
print(my_list[-3:])

# Print the list without the first two elements
print(my_list[2:])

# Print the list without the last two elements
print(my_list[:-2])

# Print the list twice in reverse without the last two elements
print(my_list[:-2] + my_list[:-2][::-1])

# Print the list in reverse
print(my_list[::-1])

# Print the list in reverse skipping every other element
print(my_list[::-2])

# Print the list in reverse skipping every other element starting from the second element
print(my_list[-2::-2])

# Print the list in reverse skipping every other element starting from the second to last element
print(my_list[-2:0:-2])

# Print the list in reverse skipping every other element starting from the third to last element
print(my_list[-3:0:-2])

# Print the list in reverse skipping every other element starting from the fourth to last element
print(my_list[-4:0:-2])

# Print the list in reverse skipping every other element starting from the fifth to last element
print(my_list[-5:0:-2])

# Print the list in reverse skipping every other element starting from the sixth to last element
print(my_list[-6:0:-2])

# Print the list in reverse skipping every other element starting from the seventh to last element
print(my_list[-7:0:-2])

